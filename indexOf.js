//Este é o metodo .indexOf
// Ele exibe o primeiro index (posição) de um valor especifico
//exemplo 1:
let objetos = ["Lampada", "Computador", "Calculadora", "Borracha"];
//Declarei a variavel objetos
let index = objetos.indexOf("Calculadora");
//declarei uma variavel e utilizei o metodo para exibir o index da palavra Calculadora

console.log(index);
//Utilizado para exibir o index
//output: 2

//Exemplo 2:
let planetas = ["Mercurio", "Venus", "Terra", "Marte"];
let posicao = planetas.indexOf("Mercurio");
// Aqui foi utilizado para mostrar o index do planeta Mercurio

console.log(posicao);
