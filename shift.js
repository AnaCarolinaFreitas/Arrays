//Este é o metodo shift
//Ao utiliza-lo, o primeiro elemento do array é removido
//Exemplo 1:
let cantores = ["Taylor Swift", "Harry Styles", "The weekend", "Bruno Mars"];
cantores.shift();

console.log(cantores);
//output: [ 'Harry Styles', 'The weekend', 'Bruno Mars' ]

//Exemplo 2:
let animais = ["Elefante", "Cachorro", "Girafa", "Gato"];
animais.shift();

console.log(animais);
//output: [ 'Cachorro', 'Girafa', 'Gato' ]