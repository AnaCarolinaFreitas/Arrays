// Este é o metodo join
//Ele permite unir elementos de um array com uma única string
//Exemplo 1:

let cores = ["Azul", "Verde", "Amarelo", "Laranja"];
let resultado = cores.join(", ");
// o (", ") utilizado junto ao join é utilizado como separador

console.log(resultado);
//output: Azul, Verde, Amarelo, Laranja.

