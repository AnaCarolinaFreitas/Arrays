//Este é o metodo length
//O metodo length permite que obtenhamos o tamanho de um array
//Exemplo 1:
let marcas = ["LG", "Amazon", "Dior"];
//Defini a variavel nomeada "marcas"
let tamanho = marcas.length;
//defini a variavel, e declarei que desejo saber o tamanho da variavel marcas

console.log("O tamanho é:" +  tamanho);
//Utilizei o comando console.log e adicionei uma frase que se somará a variavel tamanho definida anteriormente
//output: O tamanho é:3

//Exemplo 2:
let numeros = ["0", "1", "2", "3", "4", "5"];
let size = numeros.length;

console.log("O tamanho é:" + size);
//output: O tamanho é:6