//Este é o metodo sort
//Este metodo converte os elementos array em strings e os ordena
//Exemplo 1:
let nomes = ["Ana", "Luiza", "José"];
//Declarei a variavel original
nomes.sort();
//Utilizo o sort para alterar a ordem dos elementos

console.log(nomes);
//output: [ 'Ana', 'José', 'Luiza' ] 

//Exemplo 2:
let aplicativos = ["twitter", "Youtube", "Spotify", "Twitch"];
aplicativos.sort()

console.log(aplicativos);
//output: [ 'Spotify', 'Twitch', 'Youtube', 'twitter' ]